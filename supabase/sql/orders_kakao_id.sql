-- ============================================================================
-- orders.kakao_id — 주문을 "안 바뀌는 카카오 정체성"에 연결 (2026-07-07)
-- ============================================================================
-- ▶ 목적: 관리자가 전화/이름/주소를 수정해도 고객 주문 조회(내 주문)가 안 깨지게,
--   주문에 카카오 user id(kakao_id)를 스탬프. 신규 주문 = 제출 시 찍힘,
--   기존 주문 = 로그인 시 전화번호 매칭으로 소급 연결(kakao_id 없는 것만).
-- ▶ 규칙: ADD COLUMN only · IF NOT EXISTS(재실행 안전) · 기존 데이터 무변경.
-- ▶ 이 컬럼은 조회(내 주문) 필터에만 사용 — 입금매칭/정산/포인트/재고/주문상태
--   계산에는 일절 쓰이지 않음(돈 로직 무관).
-- ▶ ⚠️ 배포 전 반드시 Supabase SQL Editor에서 실행:
--   미실행 상태로 프론트가 배포되면 myorder(내 주문) 조회가
--   "column orders.kakao_id does not exist"로 실패해 고객 주문이 안 보임.
-- ============================================================================

alter table public.orders add column if not exists kakao_id text;

-- 전화번호 소급연결(로그인 시) · 조회 필터 성능용 인덱스
create index if not exists orders_kakao_id_idx on public.orders (kakao_id);
